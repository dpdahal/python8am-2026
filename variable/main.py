# rule for variable?
# 1. variable name must start with a letter or underscore(_)
# 2. variable name cannot start with a number
# 3. variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )
# 4. variable names are case-sensitive (age, Age and AGE are three different variables)
# 5. variable name cannot be a reserved word (keywords)
# 6. variable name should be meaningful
# 7. you cannot use special characters like !, @, #, $, %, etc. in variable names
# 8. you cannot use spaces in variable names. instead of spaces, you can use underscores(_)


